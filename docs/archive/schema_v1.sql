-- ------------------------------------------------------------
-- 4. drafts
-- Every email draft generated by the system.
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS drafts (
  id                  BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  draft_uid           CHAR(26) NOT NULL,               -- ULID, exposed externally
  user_id             BIGINT UNSIGNED NOT NULL,
  property            VARCHAR(60)  NOT NULL,
  task_type           VARCHAR(40)  NOT NULL,
  recipient_name      VARCHAR(200) NULL,
  recipient_context   TEXT NULL,
  input_language      ENUM('th','en') NOT NULL DEFAULT 'en',
  objective           TEXT NOT NULL,
  additional_notes    TEXT NULL,
  template_id         BIGINT UNSIGNED NULL,
  prompt_version      VARCHAR(20) NOT NULL,
  generated_subject   VARCHAR(255) NULL,
  generated_body      MEDIUMTEXT NULL,
  qc_no_em_dash       TINYINT(1) NOT NULL DEFAULT 1,
  qc_no_slang         TINYINT(1) NOT NULL DEFAULT 1,
  qc_cta_present      TINYINT(1) NOT NULL DEFAULT 1,
  qc_loyalty_ok       TINYINT(1) NOT NULL DEFAULT 1,
  qc_length_ok        TINYINT(1) NOT NULL DEFAULT 1,
  input_tokens        INT UNSIGNED NULL,
  cached_input_tokens INT UNSIGNED NULL,
  output_tokens       INT UNSIGNED NULL,
  estimated_cost_thb  DECIMAL(8,4) NULL,
  latency_ms          INT UNSIGNED NULL,
  model               VARCHAR(60)  NOT NULL DEFAULT 'claude-sonnet-4-6',
  created_at          DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id),
  UNIQUE KEY uniq_draft_uid (draft_uid),
  KEY idx_drafts_user_created (user_id, created_at DESC),
  KEY idx_drafts_recipient (recipient_name(60)),
  CONSTRAINT fk_drafts_user
    FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ------------------------------------------------------------
-- 5. templates (Phase 3)
-- Reusable starter prompts curated by MARCOM.
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS templates (
  id              BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  name            VARCHAR(120) NOT NULL,
  description     VARCHAR(255) NULL,
  task_type       VARCHAR(40)  NOT NULL,
  property_scope  VARCHAR(60)  NULL,                   -- NULL = all properties
  body            MEDIUMTEXT   NOT NULL,
  is_active       TINYINT(1)   NOT NULL DEFAULT 1,
  created_at      DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ------------------------------------------------------------
-- 6. audit_log
-- Append-only. Every meaningful action.
-- DO NOT store full draft bodies here — reference draft_uid instead.
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS audit_log (
  id          BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  user_id     BIGINT UNSIGNED NULL,
  action      VARCHAR(60) NOT NULL,
    -- 'login', 'logout', 'generate', 'regenerate',
    -- 'admin_prompt_update', 'admin_template_create',
    -- 'pdpa_delete', 'rate_limit_hit', 'error'
  draft_uid   CHAR(26) NULL,
  severity    ENUM('info','warn','error') NOT NULL DEFAULT 'info',
  metadata    JSON NULL,                               -- token usage, error details, etc.
  ip_address  VARCHAR(45) NULL,
  user_agent  VARCHAR(255) NULL,
  created_at  DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id),
  KEY idx_audit_user_created (user_id, created_at DESC),
  KEY idx_audit_action_created (action, created_at DESC),
  CONSTRAINT fk_audit_user
    FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

