-- ============================================================
-- Chatrium AI Communication Assistant — MySQL schema
-- Target: MySQL 8.0+ on Hostinger managed DB
-- Database: chatrium_ai
-- ============================================================

CREATE DATABASE IF NOT EXISTS chatrium_ai
  DEFAULT CHARACTER SET utf8mb4
  DEFAULT COLLATE utf8mb4_unicode_ci;

USE chatrium_ai;

-- ------------------------------------------------------------
-- 1. users
-- One row per Chatrium employee with access to the app.
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS users (
  id              BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  email           VARCHAR(255) NOT NULL,
  name            VARCHAR(120) NULL,
  role            VARCHAR(60)  NOT NULL DEFAULT 'staff',
    -- enum-like: 'general_manager', 'front_office_manager',
    --           'sales_marketing', 'reservations',
    --           'guest_relations', 'staff', 'marcom_admin', 'it_admin'
  property        VARCHAR(60)  NOT NULL DEFAULT 'rawai',
    -- 'rawai', 'grand_bangkok', 'riverside_bangkok',
    -- 'maitria_rama9', 'maitria_sukhumvit18'
  is_active       TINYINT(1) NOT NULL DEFAULT 1,
  created_at      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  last_login_at   DATETIME NULL,
  PRIMARY KEY (id),
  UNIQUE KEY uniq_users_email (email)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ------------------------------------------------------------
-- 2. login_tokens
-- Magic-link tokens. Hashed at rest.
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS login_tokens (
  id              BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  user_id         BIGINT UNSIGNED NOT NULL,
  token_hash      CHAR(64) NOT NULL,                   -- SHA-256 hex
  expires_at      DATETIME NOT NULL,
  used            TINYINT(1) NOT NULL DEFAULT 0,
  created_at      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id),
  KEY idx_login_tokens_user (user_id),
  KEY idx_login_tokens_hash (token_hash),
  CONSTRAINT fk_login_tokens_user
    FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ------------------------------------------------------------
-- 3. brand_voice_prompts
-- Versioned system prompt. Never UPDATE in place — INSERT new row.
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS brand_voice_prompts (
  id              BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  version         VARCHAR(20)  NOT NULL,               -- 'v1.0', 'v1.1', ...
  prompt_text     MEDIUMTEXT   NOT NULL,
  is_active       TINYINT(1)   NOT NULL DEFAULT 0,
  notes           VARCHAR(500) NULL,
  created_by      VARCHAR(120) NULL,                   -- email of MARCOM editor
  created_at      DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id),
  UNIQUE KEY uniq_prompt_version (version)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

